			<p><strong>What is MindReadr?</strong></p>
		</center>
		<p>MindReadr is a clue-based social learning game that capitalizes on mutual interests to build up nuggets of knowledge through concept recall and discovery. The game draws inspiration from games like Pictionary, Taboo, and Celebrity. The game requires two players. Players alternate in giving clues and guessing clues. Here's the catch: if you are the clue giver, you must be confident about the "answer" you are describing to the guesser (a la in Jeopardy); otherwise, you might be giving a clue for the wrong answer!</p>
		<p>We hope you enjoy playing the game as much as we had creating it!</p>
